// Avatar lookup utilities
export function getAvatarByName(name: string): IAvatarConfigItem | undefined {
  return SPEECH_AVATARS_FULL.find(avatar => avatar.name === name);
}

export function getAvailableAvatarNames(): string[] {
  return SPEECH_AVATARS_FULL.map(avatar => avatar.name);
}

export function getAvatarConfig(name: string): {
  character: string;
  style: string;
  thumbnail?: string;
  backgroundImage?: string;
} | null {
  const avatar = getAvatarByName(name);
  if (!avatar) return null;

  // Parse character and style from the avatar name
  // Format is usually "Character-style" (e.g., "Lisa-casual-sitting", "Harry-business")
  const nameParts = avatar.name.split('-');
  const character = nameParts[0].toLowerCase();
  const style = nameParts.slice(1).join('-');

  return {
    character,
    style,
    thumbnail: avatar.img,
    backgroundImage: avatar.lucencyBgImg || avatar.bgImg,
  };
}

// Get the first available avatar (fallback)
export function getDefaultAvatar(): IAvatarConfigItem {
  return SPEECH_AVATARS_FULL[0];
}

